
def add_one(number):
    return number + 1


def check_greater_than_ten(number):
    if number > 10:
        return "მაგარია!"
    else:
        return "არ არის მაგარი :("


def find_smaller_number(num1, num2):
    return min(num1, num2)


def get_text_length(text):
    return len(text)


def sum_two_numbers(num1, num2):
    return num1 + num2


def check_positive_or_negative(number):
    if number > 0:
        return "დადებითი"
    elif number < 0:
        return "უარყოფითი"
    else:
        return "ნულოვანი"


def is_even(number):
    return number % 2 == 0


def sum_plus_five(num1, num2):
    return num1 + num2 + 5
